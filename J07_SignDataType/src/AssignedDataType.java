
public class AssignedDataType {
	public static void main(String[] args) {
		
		//Ep kieu ngam dinh
		int a = 100;
		int b = 2;
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		
		
		
		float aFloat = a;
		float bFloat = b;
		
		System.out.println("a = " + aFloat);
		System.out.println("b = " + bFloat);
		
		
		//ep kieu tuong minh
		float e = 3.5f;
		float f = 9.5f;
		
		int g= (int)e;
		int h = (int)f;
		
		System.out.println("e = " + e);
		System.out.println("f = " + f);
		System.out.println("g = " + g);
		System.out.println("h = " + h);
		
		
		//Ep kieu du lieu nguyen thuy voi doi tuong
		int x = new Integer(32);
		System.out.println("X = " + x);
		
		
		
	}
}
